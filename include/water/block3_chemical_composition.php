Самоизливающаяся вода «Рычал-Су» имеет температуру от 31,5 до 36,5°С.<br>
 Общая минерализация воды «Рычал-Су» — от 4,0 до 5,0 г/л,<br>
 основные ингредиенты следующие (в мг/л):<br>
 Анионы:<br>
 гидрокарбонат HCO3– — 2500–3000<br>
 сульфат SO42− — &lt;25<br>
 хлорид Cl− — 450–550.<br>
 Катионы:<br>
 кальций Ca2+ — &lt;25<br>
 магний Mg2+ — &lt;25<br>
 натрий + калий Na++K+ — 1200–1450.<br>